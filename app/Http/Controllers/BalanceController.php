    public function update(Request $request, $id)
    {
        $student = Student::find($id);

       $sname=$request->sname;
       $student->sname=$sname;

       $age=$request->age;
       $student->age=$age;

       $rate=$request->rate;
       $student->rate=$rate;

       $group_id=$request->group_id;
       $student->group_id=$group_id;

       $student->save();
       
       return redirect()->route('students.index');
    }

